function countCoffeeList0(type)  {
	  // 결과를 표시할 element
	  const resultCount = document.getElementById('coffeeCount0');
	  const resultCartAmount = document.getElementById('coffeeCartAmount0');
	  const resultOrderAmount = document.getElementById('coffeeOrderAmount0');
	  
	  // 현재 화면에 표시된 값
	  let number = resultCount.innerText;
	  
	  // 더하기/빼기
	  if(type === 'plus') {
	    number = parseInt(number) + 1;
	  }else if(type === 'minus' && number > 0)  {
	    number = parseInt(number) - 1;
	  }
	  
	  // 결과 출력
	  resultCount.innerText = number;
	  resultCartAmount.value = resultCount.innerText
	  resultOrderAmount.value = resultCount.innerText
	}
	
